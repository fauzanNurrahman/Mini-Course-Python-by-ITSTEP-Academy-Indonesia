# looping kita gunakan untuk mengulang elemen yang dalam colection

buah = ["apel","jeruk","mangga"]

for item in buah:
    if item == "apel":
        print("Kamu adalah apel")

numbers = [1,2,3,4,5,6]
for number in numbers:
    if number % 2 == 0:
        print(number, "Kmau adalah angka genap")
    else:
        print(number,"Kamu adalah angka ganjil")